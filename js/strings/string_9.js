/* Create a variable word and assign it the value "JavaScript".
Use bracket notation ([]) to:
Print the first character of the string.
Print the last character of the string.
Print both results using console.log().
Example output:
"First character: J"
"Last character: t" */
let word = "JavaScript";
let firstCharacter = word[0];
let lastCharacter = word.length - 1;
let lastVisibleNumber = word[lastCharacter];
console.log(
  "First character: " + firstCharacter,
  "Last character: " + lastVisibleNumber
);
